let x = Math.floor(Math.random() * 100 + 1);
let y = Math.floor(Math.random() * 50 + 1);

const sum = `${x}+${y} = ${x + y}`;
console.log('sum :', sum);
const diff = `${x}-${y} = ${x - y}`;
console.log('diff :', diff);
const prod = `${x}*${y}=${x * y}`;
console.log('prod :', prod);
const div = `${x}/${y}=${x / y}`;
console.log('div :', div);
const remainder = `${x}%${y}=${x % y}`;
console.log('remainder :', remainder);
